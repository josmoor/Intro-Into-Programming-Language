/**
 * Point object that holds two different Integer values. The values are set to public for
 * simple manipulation use.
 * 
 * @author Josh Moore
 */
public class Point {

    // Variables
    public int x, y;

    /**
     * Constructor of the Point Object, Intakes two integers that are set
     * to the stored integer objects.
     * 
     * @param x - X Integer Value
     * @param y - Y Integer Value
     */
    public Point(int x, int y) {
        this.x = x;
        this.y = y;
    }
}
